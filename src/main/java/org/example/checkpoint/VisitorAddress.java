package org.example.checkpoint;

public class VisitorAddress {
    public String addressLine;
    public String boxAddressLine;
    public String postPlace;
    public String zipCode;

    public String getAddressLine() {
        return addressLine;
    }

    public void setAddressLine(String addressLine) {
        this.addressLine = addressLine;
    }

    public String getBoxAddressLine() {
        return boxAddressLine;
    }

    public void setBoxAddressLine(String boxAddressLine) {
        this.boxAddressLine = boxAddressLine;
    }

    public String getPostPlace() {
        return postPlace;
    }

    public void setPostPlace(String postPlace) {
        this.postPlace = postPlace;
    }

    public String getZipCode() {
        return zipCode;
    }

    public void setZipCode(String zipCode) {
        this.zipCode = zipCode;
    }
}
